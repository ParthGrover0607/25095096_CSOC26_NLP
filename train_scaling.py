def validate(model, loader):

    num_batches= 0
    total_loss=0
    model.eval()

    with torch.no_grad():
        for x,y in loader:
            x_input= x.to(device)
            y_input= y.reshape(-1).to(device)
            outputs= model(x_input)
            preds= outputs.reshape(-1, outputs.shape[-1])

            loss= criterion(preds, y_input)
            total_loss+= loss.item()

            num_batches+=1

    val_loss_hist.append(total_loss/num_batches)
    return total_loss/num_batches

def train(model, dataloader, epochs=10):

    steps= 0
    total_loss=0
    best_val_loss= 1e4
    model.train()

    for epoch in range(epochs):
        for x,y in dataloader:

            x_input= x.to(device)
            y_input= y.reshape(-1).to(device)

            optimizer.zero_grad()
            outputs = model(x_input)
            outputs = outputs.reshape(-1, outputs.shape[-1])

            loss = criterion(outputs, y_input)
            loss.backward()
            torch.nn.utils.clip_grad_norm_(model.parameters(), 1.0)
            optimizer.step()
            total_loss = loss.item()

            if(steps%200==0):
                print(f"Operation {steps+1}, Loss: {total_loss}")
            loss_hist.append(total_loss)

            if(steps%300 == 0):
                val_loss= validate(model,val_loader)
                best_val_loss= min(val_loss, best_val_loss)

            steps +=1

            if(steps >=3000):
                break
        if(steps >=3000):
            break

    torch.save(model.state_dict(),f'model_step{steps+1}.pth')
    return best_val_loss

