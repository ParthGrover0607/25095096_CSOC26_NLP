# create a custom dataset for this tinyShakes dataset
class CustomDataset(Dataset):

    def __init__(self, tokens, T):
        self.tokens = tokens
        self.T = T

    def __len__(self):
        return len(self.tokens) - self.T

    def __getitem__(self, idx):

        x = self.tokens[idx:idx+self.T]
        y = self.tokens[idx+1:idx+self.T+1]

        return x, y

